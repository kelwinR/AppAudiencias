package model;

/**
 *
 * @author kelwin.rodrigues
 */
public class Parte {
    
    private int id;
    private String nome;
    private String endereco;
    private int idade;
    private String complemento;
    private int id_tipo_parte;
    private int id_cidade;
    private int id_processo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public int getId_tipo_parte() {
        return id_tipo_parte;
    }

    public void setId_tipo_parte(int id_tipo_parte) {
        this.id_tipo_parte = id_tipo_parte;
    }

    public int getId_cidade() {
        return id_cidade;
    }

    public void setId_cidade(int id_cidade) {
        this.id_cidade = id_cidade;
    }

    public int getId_processo() {
        return id_processo;
    }

    public void setId_processo(int id_processo) {
        this.id_processo = id_processo;
    }

    
}

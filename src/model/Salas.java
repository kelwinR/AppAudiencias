package model;

/**
 *
 * @author kelwin.rodrigues
 */
public class Salas {
    
    private int id;
    private String nome;
    private boolean numero;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public boolean isNumero() {
        return numero;
    }

    public void setNumero(boolean numero) {
        this.numero = numero;
    }
    
    
}

    private void limparTela(){
        lblId.setText("ID");
        txtNum.setText("");;
        jcbOrgJulg.setSelectedIndex(0);
        jcbClassAcao.setSelectedIndex(0);
        jcbComp.setSelectedIndex(0);
        jcbMagis.setSelectedIndex(0);
        atualizarTabela();
    }
    
    private boolean validarDados(){
        if(txtNum.getText().trim().equals("")){ 
            CaixaDeDialogo.obterinstancia().exibirMensagem("Informe uma cidade");
            return false;
        }
        
        if(jcbOrgJulg.getSelectedIndex() <= 0){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Escolha um orgão julgador");
            return false;
        }
        
         if(jcbClassAcao.getSelectedIndex() <= 0){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Escolha uma classe de ação");
            return false;
        }
         
          if(jcbComp.getSelectedIndex() <= 0){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Escolha a competência");
            return false;
        }
          
         if(jcbMagis.getSelectedIndex() <= 0){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Escolha um magistrado");
            return false;
        }
      
        return true;
    }
    
    private Processo guardarDados(){
        try{
            Processo objeto = new Processo();
            
            objeto.setNum(Integer.parseInt(txtNum.getText().trim()));
            
            
            Combo c = (Combo) jcbOrgJulg.getSelectedItem();
            int cod_estado = Integer.parseInt(c.getCodigo());
            objeto.setId_org_julg(cod_estado);
            
             Combo a = (Combo) jcbClassAcao.getSelectedItem();
            int cod_class_acao = Integer.parseInt(a.getCodigo());
            objeto.setId_class_acao(cod_class_acao);
            
             Combo b = (Combo) jcbComp.getSelectedItem();
            int cod_comp = Integer.parseInt(b.getCodigo());
            objeto.setId_comp(cod_comp);
            
             Combo d = (Combo) jcbMagis.getSelectedItem();
            int cod_magis = Integer.parseInt(d.getCodigo());
            objeto.setId_magistrado(cod_magis);
            
            return objeto;
            
        }catch(Exception ex){
            return null;
        }
    }
    
      private void preencherCampos(Processo objeto){
        try{
            
            lblId.setText(String.valueOf(objeto.getId()));
            txtNum.setText(String.valueOf(objeto.getNum()));
            
            comboOrgJulg.setaComboBox(String.valueOf(objeto.getId_org_julg()));
            comboClassAcao.setaComboBox(String.valueOf(objeto.getId_class_acao()));
            comboComp.setaComboBox(String.valueOf(objeto.getId_comp()));
            comboMagis.setaComboBox(String.valueOf(objeto.getId_magistrado()));
            
        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem(ex.getMessage(), 'e');
        }
    }
      
      private void atualizarTabela() {
        try {
           ProcessoController objProcessoController = new ProcessoController();
            objProcessoController.preencher(jtbProcessos);

        } catch (Exception ex) {
            CaixaDeDialogo.obterinstancia().exibirMensagem("ERRO:" + ex.getMessage());
        }
    }
      
    

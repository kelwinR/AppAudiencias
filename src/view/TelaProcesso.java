    private void jblSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jblSalvarActionPerformed
        // TODO add your handling code here:
          boolean retorno = validarDados();
        if(retorno){
            
            Processo processo = guardarDados();
            //SALVAR NO BANCO DE DADOS
            
           ProcessoController objProcessoController = new ProcessoController();
           
           if(lblId.getText().equals("ID")){
               String ret = objProcessoController.incluir(processo);
               if(ret.equals("")){
                   CaixaDeDialogo.obterinstancia().exibirMensagem("Processo incluído com sucesso");
                   limparTela();
           }else{
                   CaixaDeDialogo.obterinstancia().exibirMensagem(ret);
               }
          
        }else{
           processo.setId(Integer.parseInt(lblId.getText()));
           boolean ret = objProcessoController.alterar(processo);
           if(ret){
               CaixaDeDialogo.obterinstancia().exibirMensagem("Processo alterado com sucesso");
               limparTela();
           }else{
                CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao alterar processo");
           }
        }
        }
        
        atualizarTabela();
    }//GEN-LAST:event_jblSalvarActionPerformed

    private void jtbProcessosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbProcessosMousePressed
        // TODO add your handling code here:
         try {
            //pega a linha selecionada
            int linhaSelecionada = jtbProcessos.getSelectedRow();

            // Primeira coluna da linha
            String codigo = jtbProcessos.getModel().getValueAt(linhaSelecionada, 0).toString();

            //buscar no banco de dados o registro e preencher nos campos da tela
           ProcessoController objProcessoController = new ProcessoController();
            Processo objeto = objProcessoController.buscar(codigo);
                
            //Verifica se clicou na coluna 2 => EXCLUIR
            if (jtbProcessos.isColumnSelected(2)) {
                try {
                    boolean wPergunta = CaixaDeDialogo.obterinstancia()
                            .pedirConfirmacao("Tem certeza de que deseja excluir?", "", 'p');
                    if (wPergunta == true) {
                        //exclusão do registro selecionado
                        objProcessoController = new ProcessoController();
                        boolean retorno = objProcessoController.excluir(codigo);
                        if(retorno){
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Registro excluído com sucesso");
                            atualizarTabela();
                        }else{
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao excluir");
                        }
                    }                    

                } catch (Exception ex) {
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro: " + ex.getMessage());
                }
            } else {
                if (objeto != null) {
                    preencherCampos(objeto);
                }
            }


        } catch (Exception ex) {
            CaixaDeDialogo.obterinstancia().exibirMensagem(ex.getMessage(), 'e');
        }
        
        
        
    }//GEN-LAST:event_jtbProcessosMousePressed

